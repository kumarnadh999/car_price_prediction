import streamlit as st
import pandas as pd
import matplotlib.pyplot as plt

df = pd.read_csv('used_cars_data (1).csv')

st.write("Basic Statistics")
st.write(df.describe())

st.write("Distribution of Car Prices")
plt.figure(figsize=(10, 6))
plt.hist(df['Price'], bins=30)
plt.xlabel('Price')
plt.ylabel('Frequency')
plt.title('Distribution of Car Prices')
st.pyplot(plt)

st.write("Distribution of Car Ages")
df['Age'] = 2023 - df['Year']
plt.figure(figsize=(10, 6))
plt.hist(df['Age'], bins=30)
plt.xlabel('Age')
plt.ylabel('Frequency')
plt.title('Distribution of Car Ages')
st.pyplot(plt)

st.write("Distribution of Car Mileage")
df['Mileage'] = df['Mileage'].str.extract('(\d+\.\d+)').astype(float)
plt.figure(figsize=(10, 6))
plt.hist(df['Mileage'], bins=30)
plt.xlabel('Mileage')
plt.ylabel('Frequency')
plt.title('Distribution of Car Mileage')
st.pyplot(plt)

st.write("Distribution of Car Power")
df['Power'] = df['Power'].str.extract('(\d+\.\d+)').astype(float)
plt.figure(figsize=(10, 6))
plt.hist(df['Power'], bins=30)
plt.xlabel('Power')
plt.ylabel('Frequency')
plt.title('Distribution of Car Power')
st.pyplot(plt)

st.write("Distribution of Car Engine Size")
df['Engine'] = df['Engine'].str.extract('(\d+)').astype(float)
plt.figure(figsize=(10, 6))
plt.hist(df['Engine'], bins=30)
plt.xlabel('Engine Size')
plt.ylabel('Frequency')
plt.title('Distribution of Car Engine Size')
st.pyplot(plt)